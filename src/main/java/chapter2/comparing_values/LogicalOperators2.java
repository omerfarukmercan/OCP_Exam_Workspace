package chapter2.comparing_values;

public class LogicalOperators2 {
    public static void main(String[] args) {

        boolean bool1 = true;
        boolean bool2 = true;

        System.out.println(bool1 | bool2);
        System.out.println(bool1 & bool2);
        System.out.println(bool1 ^ bool2);

    }
}
